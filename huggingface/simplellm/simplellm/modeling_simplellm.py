class SimpleAttention(nn.Module):
    """"Attention as in the paper "Attention is All you need"
    does not include multihead;
    Only implements $softmax(QK^T/\sqrt {d_k}) V$, and use casual mask
    input:
        embed_dim: hidden dimension of the linear modules
    """
    def __init__(self, config: SimpleLLMConfig, layer_idx: Optional[int] = None):
        super().__init__()
        self.config = config
        self.layer_idx = layer_idx
        self.hidden_size = config.hidden_size
        self.q_proj = nn.Linear(self.hidden_size, self.hidden_size, bias=False)
        self.k_proj = nn.Linear(self.hidden_size, self.hidden_size, bias=False)
        self.v_proj = nn.Linear(self.hidden_size, self.hidden_size, bias=False)

    
    def forward(self, 
            hidden_states: torch.Tensor,
            attention_mask: Optional[torch.Tensor] = None,
            output_attentions: bool = False
            ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        query_states = self.q_proj(hidden_states)
        key_states = self.k_proj(hidden_states)
        value_states = self.v_proj(hidden_states)

        attn_weights = query_states @ key_states.transpose(-1, -2) / math.sqrt(self.hidden_size)

        if attention_mask is not None:  # no matter the length, we just slice it
            causal_mask = attention_mask[:, :, :, : key_states.shape[-2]]
            attn_weights = attn_weights + causal_mask

        attn_weights = F.softmax(attn_weights, dim=-1, dtype=torch.float32).to(query_states.dtype)
        attn_output = attn_weights @ value_states

        if not output_attentions:
            attn_weights = None

        return attn_output, attn_weights


class SimpleMLP(nn.Module):
    def __init__(self, config: SimpleLLMConfig):
        super().__init__()
        self.ffn_dim = config.intermediate_size
        self.hidden_dim = config.hidden_size
        self.fc = nn.Linear(self.hidden_dim, self.ffn_dim, bias=False)
        self.fo = nn.Linear(self.ffn_dim, self.hidden_dim, bias=False)
        self.act_fn = ACT2FN[config.hidden_act]


    def forward(self,
            hidden_states: torch.Tensor
            ):
        return  self.fo(self.act_fn(self.fc(hidden_states)))


class SimpleDecoderLayer(nn.Module):
    def __init__(self, config: SimpleLLMConfig, layer_idx: int):
        super().__init__()
        self.hidden_size = config.hidden_size

        self.self_attn = SimpleAttention(config, layer_idx)
        self.mlp = SimpleMLP(config)

    def forward(self,
            hidden_states: torch.Tensor,
            attention_mask: Optional[torch.Tensor] = None,
            output_attentions: Optional[bool] = False
            ) -> Tuple[torch.Tensor, Optional[torch.Tensor]]:
        residual = hidden_states
        hidden_states, self_attn_weights = self.self_attn(
                hidden_states = hidden_states,
                attention_mask = attention_mask,
                output_attentions = output_attentions
        )
        hidden_states = residual + hidden_states
        
        residual = hidden_states
        hidden_states = self.mlp(hidden_states)
        hidden_states = residual + hidden_states
        outputs = (hidden_states,)

        if output_attentions:
            outputs += (self_attn_weights,)

        return outputs

